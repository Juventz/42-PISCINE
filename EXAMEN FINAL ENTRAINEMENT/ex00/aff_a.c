#include <unistd.h>

void	aff_a(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, "a\n", 2);
		i++;
	}
}

int	main(int argv, char **argc)
{
	(void)argc;
	(void)argv;
	write(1, "a\n", 2);
	return (0);
}
