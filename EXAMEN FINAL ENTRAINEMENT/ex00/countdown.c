#include <unistd.h>

void	ft_countdown(void)
{
	int	a;
	int	b;

	a = '0';
	b = '9';
	while (a <= b)
	{
		write(1, &b, 1);
		b--;
	}
	write(1, "\n", 1);
}

int	main(void)
{
	ft_countdown();
}
