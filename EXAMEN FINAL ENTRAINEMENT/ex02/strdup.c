#include <stdlib.h>
#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while(str[i] != '\0')
		i++;
	return (i);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while(str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while(src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	int	size;
	char	*copy;

	size = ft_strlen(src);
	copy = malloc(sizeof(char) * size);
	if (!(copy))
		return (NULL);
	ft_strcpy(copy, src);
	return (copy);
	
}

int	main(int argc, char **argv)
{
	(void)argc;
	ft_putstr(ft_strdup(argv[1]));
	return (0);
}
